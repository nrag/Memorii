_GOLDEN_CREATED_AT = datetime(2026, 1, 1, tzinfo=UTC)


def _promotion_snapshot(
    *,
    snapshot_id: str,
    candidate_id: str,
    candidate_type: PromotionCandidateType,
    content: str,
    created_from: str,
    expected_output: dict[str, object],
    tags: list[str],
    repeated_across_episodes: int = 0,
    explicit_user_memory_request: bool = False,
    related_memory_ids: list[str] | None = None,
    source_ids: list[str] | None = None,
    metadata: dict[str, object] | None = None,
) -> EvalSnapshot:
    return EvalSnapshot(
        snapshot_id=snapshot_id,
        decision_point=LLMDecisionPoint.PROMOTION,
        input_payload={
            "candidate_id": candidate_id,
            "candidate_type": candidate_type.value,
            "content": content,
            "source_ids": source_ids or [f"src:{snapshot_id}"],
            "created_from": created_from,
            "repeated_across_episodes": repeated_across_episodes,
            "explicit_user_memory_request": explicit_user_memory_request,
            "related_memory_ids": related_memory_ids or [],
            "metadata": metadata
            or {
                "golden_version": "promotion_v1",
                "curation_tier": "reviewed",
                "llm_followup_expected": bool(expected_output.get("requires_judge_review") is True),
            },
        },
        expected_output=expected_output,
        source="offline_golden",
        tags=tags,
        created_at=_GOLDEN_CREATED_AT,
    )


def _belief_snapshot(
    *,
    snapshot_id: str,
    prior_belief: float | None,
    decision: SolverDecision,
    evidence_count: int,
    missing_evidence_count: int = 0,
    verifier_downgraded: bool = False,
    conflict_count: int = 0,
    expected_output: dict[str, object] | None,
    tags: list[str],
    hypothesis: str,
    scenario: str,
    evidence_summary: str,
    missing_evidence_summary: str,
) -> EvalSnapshot:
    evidence_ids = [f"ev:{snapshot_id}:{idx + 1}" for idx in range(evidence_count)] if evidence_count > 0 else []
    missing_evidence = (
        [f"missing:{snapshot_id}:{idx + 1}" for idx in range(missing_evidence_count)] if missing_evidence_count > 0 else []
    )

    context = BeliefUpdateContext(
        prior_belief=prior_belief,
        decision=decision,
        evidence_count=evidence_count,
        missing_evidence_count=missing_evidence_count,
        verifier_downgraded=verifier_downgraded,
        conflict_count=conflict_count,
        evidence_ids=evidence_ids,
        missing_evidence=missing_evidence,
        metadata={
            "hypothesis": hypothesis,
            "scenario": scenario,
            "evidence_summary": evidence_summary,
            "missing_evidence_summary": missing_evidence_summary,
            "golden_version": "belief_v1",
            "curation_tier": "reviewed",
        },
    )

    return EvalSnapshot(
        snapshot_id=snapshot_id,
        decision_point=LLMDecisionPoint.BELIEF_UPDATE,
        input_payload=context.model_dump(mode="json"),
        expected_output=expected_output,
        source="offline_golden",
        tags=tags,
        created_at=_GOLDEN_CREATED_AT,
    )


